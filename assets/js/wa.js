function wastatic() {
    // Tangkap data dari konten
    const pesan = document.querySelector("#wastatic").textContent;
    // Masukkan data ke URL WhatsApp
    const whatsappUrl = "https://wa.me/[6281339731979]?text=" + encodeURIComponent(pesan);
  
    // Update tombol WhatsApp
    const whatsappButton = document.querySelector(".whatsapp-button");
    whatsappButton.href = whatsappUrl;
  
    // Dialog konfirmasi
    const confirmDialog = confirm("Apakah Anda yakin ingin menghubungi kami melalui WhatsApp?");
  
    if (confirmDialog) {
        // Arahkan ke WhatsApp
        window.location.href = whatsappUrl;
    }
}
